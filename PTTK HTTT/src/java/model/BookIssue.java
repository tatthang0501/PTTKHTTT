package model;

/**
 *
 * @author tatth
 */
public class BookIssue {
    private int id;
    private Issue issue;

    public BookIssue() {
    }

    public BookIssue(int id, Issue issue) {
        this.id = id;
        this.issue = issue;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Issue getIssue() {
        return issue;
    }

    public void setIssue(Issue issue) {
        this.issue = issue;
    }
    
}
