package model;

/**
 *
 * @author tatth
 */
public class BookAuthor {
    private int id;
    private Author author;

    public BookAuthor() {
    }

    public BookAuthor(int id, Author author) {
        this.id = id;
        this.author = author;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Author getAuthor() {
        return author;
    }

    public void setAuthor(Author author) {
        this.author = author;
    }
    
    
}
